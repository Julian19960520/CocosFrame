const {ccclass, property} = cc._decorator;

@ccclass
export default class Damager extends cc.Component {
    @property
    private dmg: number = 1;

    public attack(hper:Hper){
        hper.Hp -= this.dmg;
        if(hper.Hp <= 0){
            this.node.emit("onKillHper");
        }
    }
}
