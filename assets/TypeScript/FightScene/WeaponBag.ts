const {ccclass, property} = cc._decorator;

@ccclass
export default class WeaponBag extends DB.DataBindComponent {
    @property(ScrollList)
    scrollList:ScrollList = null;

    onLoad () {
        this.Bind("user/weaponDatas", (weapons)=>{
            this.scrollList.setDataArr(weapons);
        });
    }

}
