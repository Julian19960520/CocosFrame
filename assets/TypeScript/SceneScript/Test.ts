const {ccclass, property} = cc._decorator;

@ccclass
export default class NewClass extends cc.Component {

    @property(cc.Label)
    label: cc.Label = null;

    @property
    text: string = 'hello';

    // LIFE-CYCLE CALLBACKS:

    // onLoad () {}

    onLoad () {
        cc.tween(this.node)
        // 同时对 scale, position, rotation 三个属性缓动
        .to(1, { scale: 2, position: cc.v2(100, 100), rotation: 90 })
        .start()
    }

    // update (dt) {}
}
